    private void autenticar() {
        Perfil perfil;
        perfil = ClienteServ.autenticarUsuario(jTextField1.getText(), new String(jPasswordField1.getPassword()));

        if (perfil != null && perfil.getPermiso() == 'c') { //cliente
            principal.getjCliente().setEnabled(true);
            principal.getjAdmin().setEnabled(false);
            JOptionPane.showMessageDialog(this, perfil.getNombre() + " autenticado con éxito", "EXITO", JOptionPane.INFORMATION_MESSAGE);

        } else if (perfil != null && perfil.getPermiso() == 'a') { //administrador
            principal.getjCliente().setEnabled(false);
            principal.getjAdmin().setEnabled(true);
            JOptionPane.showMessageDialog(this, perfil.getNombre() + " autenticado con éxito", "EXITO", JOptionPane.INFORMATION_MESSAGE);

        } else { //autenticacion fallida
            principal.getjCliente().setEnabled(false);
            principal.getjAdmin().setEnabled(false);
            JOptionPane.showMessageDialog(this, "Credenciales invalidas", "FAIL", JOptionPane.ERROR_MESSAGE);
        }
        dispose();
    }

    private void jPasswordField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            autenticar();
        }
    }//GEN-LAST:event_jPasswordField1KeyPressed


